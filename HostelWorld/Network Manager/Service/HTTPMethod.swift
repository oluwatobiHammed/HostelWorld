// Enumeration representing common HTTP methods.
public enum HTTPMethod: String {
    // HTTP GET method.
    case get = "GET"

    // HTTP POST method.
    case post = "POST"

    // HTTP PUT method.
    case put = "PUT"

    // HTTP PATCH method.
    case patch = "PATCH"

    // HTTP DELETE method.
    case delete = "DELETE"
}


